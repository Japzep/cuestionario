// Función para normalizar el texto eliminando tildes, mayúsculas y espacios
function normalizarTexto(texto) {
    return texto
        .normalize('NFD') // Separa las letras de los acentos
        .replace(/[\u0300-\u036f]/g, '') // Elimina los acentos
        .trim() // Elimina espacios en blanco al principio y final
        .toLowerCase(); // Convierte todo a minúsculas
}

function validarRespuestas() {
    // Respuestas correctas en texto plano
    const respuestasCorrectas = [
        'whisky',    // Respuesta correcta para la pregunta 1
        'serpiente',    // Respuesta correcta para la pregunta 2
        'kraus',        // Respuesta correcta para la pregunta 3
        'birria',       // Respuesta correcta para la pregunta 4
        'blanco',       // Respuesta correcta para la pregunta 5
        'chocolates',    // Respuesta correcta para la pregunta 6
        'a la orilla de un palmar'          // Respuesta correcta para la pregunta 7
    ];

    // Capturar respuestas del usuario seleccionadas en los <select>
    const respuestasUsuario = [
        document.getElementById('pregunta1').value,
        document.getElementById('pregunta2').value,
        document.getElementById('pregunta3').value,
        document.getElementById('pregunta4').value,
        document.getElementById('pregunta5').value,
        document.getElementById('pregunta6').value,
        document.getElementById('pregunta7').value
    ];

    // Validar si todas las respuestas están llenas
    for (let i = 0; i < respuestasUsuario.length; i++) {
        if (!respuestasUsuario[i]) { // Si algún campo está vacío
            alert(`Por favor, selecciona una respuesta para la pregunta ${i + 1}.`);
            return; // Salir de la función si hay algún campo vacío
        }
    }

    let correcto = true;

    // Validar cada respuesta del usuario con la correcta
    for (let i = 0; i < respuestasCorrectas.length; i++) {
        if (respuestasUsuario[i] !== respuestasCorrectas[i]) {
            correcto = false;
            break;
        }
    }

    if (correcto) {
        // Redirigir al enlace si todas las respuestas son correctas
        window.location.href = 'https://wa.link/svz6do'; 
    } else {
        alert("Incorrecto. Revisa tus respuestas e inténtalo nuevamente.");
    }
}
